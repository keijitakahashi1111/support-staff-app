def check_escalation(question):
    """質問内容からエスカレーションが必要かチェック"""
    question_lower = question.lower()
    matched = []
    for kw in ESCALATION_KEYWORDS:
        if kw in question_lower:
            matched.append(kw)

    if not matched:
        return False, [], None

    # カテゴリ判定
    harassment_kw = {"ハラスメント", "パワハラ", "セクハラ", "マタハラ", "いじめ", "嫌がらせ", "暴言", "暴力"}
    mental_kw = {"うつ", "鬱", "メンタル", "精神的につらい", "限界", "死にたい", "辞めたい", "退職したい", "休職したい"}
    compliance_kw = {"不正", "横領", "情報漏洩", "個人情報流出", "労基", "労働基準監督署", "訴訟", "裁判"}
    accident_kw = {"虐待", "事故", "怪我", "けが"}

    matched_set = set(matched)
    if matched_set & mental_kw:
        return True, matched, "メンタルヘルス相談"
    elif matched_set & harassment_kw:
        return True, matched, "ハラスメント相談窓口"
    elif matched_set & compliance_kw:
        return True, matched, "コンプライアンス通報"
    elif matched_set & accident_kw:
        return True, matched, "事故・緊急連絡"
    else:
        return True, matched, "人事・労務相談"


