def get_grade(city_code: str) -> str:
    """市町村番号から級地を取得"""
    # 完全一致を試行
    if city_code in CITY_GRADE_MAP:
        return CITY_GRADE_MAP[city_code]
    # 前方一致（先頭4桁）を試行 — 政令市対応
    prefix = city_code[:4]
    for code, grade in CITY_GRADE_MAP.items():
        if code.startswith(prefix):
            return grade
    return DEFAULT_GRADE


def get_unit_price(city_code: str, service_kind: str) -> float:
    """市町村番号とサービス種類から1単位あたりの単価(円)を取得"""
    grade = get_grade(city_code)
    kind_info = SERVICE_KIND_MAP.get(service_kind, {})
    price_key = kind_info.get("price_key", "就労移行支援")
    return GRADE_UNIT_PRICES.get(grade, GRADE_UNIT_PRICES["その他"]).get(price_key, 10.00)


def get_service_code_info(code: str) -> dict:
    """サービスコードから情報を取得"""
    return SERVICE_CODE_MASTER.get(code, {
        "name": f"不明コード({code})",
        "units": 0,
        "calc_type": "daily",
        "service_kind": "1601",
    })


def truncate(value: float) -> int:
    """小数点以下切り捨て（端数処理）"""
    import math
    return math.floor(value)
