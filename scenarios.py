def get_scenarios_by_category(category):
    """カテゴリ別にシナリオを取得"""
    return [s for s in SCENARIOS if s["category"] == category]


def get_scenarios_by_difficulty(difficulty, category=None):
    """難易度別にシナリオを取得"""
    result = [s for s in SCENARIOS if s["difficulty"] == difficulty]
    if category:
        result = [s for s in result if s["category"] == category]
    return result


def get_scenario_by_id(scenario_id):
    """IDからシナリオを取得"""
    for s in SCENARIOS:
        if s["id"] == scenario_id:
            return s
    return None


def get_categories():
    """カテゴリ一覧"""
    return ["外交活動", "利用者獲得"]


def get_difficulties():
    """難易度一覧"""
    return ["初級", "中級", "上級"]


def get_difficulty_icon(difficulty):
    """難易度のアイコン"""
    icons = {"初級": "🟢", "中級": "🟡", "上級": "🔴"}
    return icons.get(difficulty, "⚪")


